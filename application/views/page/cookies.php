광고 쿠키란 무엇인가?
광고 쿠키란 개인들의 웹 브라우징 습관을 추적하여 관심있는 물건들을 구매하도록 개인들을 타케팅합니다. 예를 들어, 사용자가 웹에서 골프 클럽들을 브라우저 하고 있고, 사용하는 웹사이트들에 광고 쿠기들이 설치되어 있다면, 사용자는 골프 제품들을 파는 또는 골프 관련 컨텐츠가 실린 다른 사이트들의 팝업 광고를 보게 됩니다. 앨리슨은 자사의 웹 사이트에서 광고 쿠키를 사용하지 않고, 타사가 그러한 종류의 쿠키를 자사의 웹사이트에서 사용하는 것을 허용하지 않습니다.

